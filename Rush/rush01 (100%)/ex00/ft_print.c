#include "rush01.h"

void	ft_print(char **tab, int max)
{
	int	i;
	int	j;

	i = 0;
	while (i < max)
	{
		j = 0;
		while (j < max)
		{
			write(1, &tab[i][j], 1);
			j++;
			if (j < max)
				write(1, " ", 1);
		}
		write(1, "\n", 1);
		i++;
	}
}
