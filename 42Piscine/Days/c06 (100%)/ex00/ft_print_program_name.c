#include <unistd.h>

void	ft_putchar(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
}

int	main(int argc, char **argv)
{
	if (argc < 1)
		return (0);
	ft_putchar(argv[0]);
	write(1, "\n", 1);
	return (0);
}
