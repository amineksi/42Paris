int	ft_atoi(char *str)
{
	int	i;
	int	n;
	int	final_result;

	i = 0;
	n = 1;
	final_result = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			n *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		final_result *= 10;
		final_result += str[i] - '0';
		i++;
	}
	return (n * final_result);
}
